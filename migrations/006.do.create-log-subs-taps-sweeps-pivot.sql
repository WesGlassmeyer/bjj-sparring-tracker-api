CREATE TABLE log_subs_taps_sweeps_pivot (
    
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    log_entry_id INTEGER REFERENCES log_entry(id) ON DELETE CASCADE,
    sub_id INTEGER REFERENCES submissions(id) ON DELETE CASCADE,
    tap_id INTEGER REFERENCES taps(id) ON DELETE CASCADE,
    sweep_id INTEGER REFERENCES sweeps(id) ON DELETE CASCADE
  
);